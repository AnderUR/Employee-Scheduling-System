    <br><br><h3><b>Semester Table</b></h3>
    Semesters that have been added using the <b>Add Semester form</b> will show in this table. 
    Each semester is editable by clicking the corresponding edit button in the Edit column.
    This will open a popup with forms to edit the semester, add exceptions dates for that semester and edit exception announcements.
