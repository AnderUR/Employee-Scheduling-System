    <br><br><h3><b>Add Semester Form</b></h3>
    In order to add shifts for employees, the given semester needs to be available to select it in the Employee page. 
    The Add Semester form allows the user to enter semesters, which will be visible in the Semesters Table and the Employee page > Schedule Shifts tab.
    The Semester can be edited from the Semester Table. 
    
    <br><br><h3><b>Calendar URL</b></h3>
    The calendar url can be filled with the PDF link from CUNY for the given semester the user wants to enter. 
    This link will be used in the calendar icon in the Employee Page > Schedule Shifts tab. <br>
    Example of link: http://www.citytech.cuny.edu/registrar/docs/fallcal_2017.pdf