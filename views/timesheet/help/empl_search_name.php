    <br><br><h3><b>Employee Search</b></h3>
    Search for an employee by typing his/her name, which will activate an auto-complete function that will reveal users to choose from. Select the name and the page will reload. 
    Now the sidebar tabs, Scheduled Shifts, Worked Shifts and Profile will refer to this employee.
    
    <br><br><h3><b>Displayed Name</b></h3>
    This name defaults to the logged in user when clicking the Employee link in the navigation bar. It refers to whose data is being viewed in this page.
