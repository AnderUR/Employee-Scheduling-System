    <br><br><h3><b>Schedule Table</b></h3>
    This table shows all the shifts the employees have to work for the current week. The data inside comes from shifts scheduled by the supervisors through the <b>Add Shift form in the Employee page</b>.  
    
    <br><br><h4><b>Shifts Column</b></h4>
    This column is only available if the sorting method is set to Employee. Supervisors can click the view link to be <b>directed to the Employee page > Schedule Shifts Tab for the given employee</b> to make changes to the shift.
