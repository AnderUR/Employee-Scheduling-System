    <br><br><h3><b>Edit Timesheet Form</b></h3>
	<b>Opens from the Status page > Status Table edit link and from the Employee page > Timesheet Table edit link.</b><br><br>
	
	This form is used to edit a shift that has at least been signed in. 
	Making changes to this form will affect the timesheet tables and the total hours the employee has worked.

    <br><br><h3><b>Notes</b></h3>	
	Notes can be written in this form to explain reasons for changes to the timesheet. An icon will appear in the Employee Timesheet Table if a note is present for the shift. 
	Notes can be edited by opening this form for the associated shift.
	
