    <br><br><h3><b>Set In Timesheet (Status page)</b></h3>
    <b>Opens from the Status page > Status Table edit link > Edit Shift form > Set in timesheet button</b><br><br>
    
	This form opens because there is no sign in time for the given worked shift. 
	By default, the date, start time, end time and location are those that were originally set when the shift was added to the schedule. 
	The goal is that, if necessary, the corresponding shift in the timesheet can be immediately filled with the original values using this form. 
	In addition, only the sign in time can be filled, so that if there was a problem with the sign in, the employee can do his/her shift and sign out later. 
	Edits can be made to any of the fields before marking the shift as worked.<br><br>
	
	Clicking <b>Mark as worked</b> will use the date, start time, end time and location of this form fields to fill the timesheet for this shift.<br><br>
	
	Clicking <b>Mark as signed in</b> will use the date, start time, and location of the original scheduled shift to fill the timesheet for this shift. The employee will then have to sign out at the end of his/her shift.
	
