    <br><br><h3><b>Set In Timesheet (Employee page)</b></h3>
    <b>Opens from the Employee page > Worked Shifts tab > Semester Timesheet Table edit link</b><br><br>
	This form opens because there is no sign in time for the given worked shift. 
	By default, the date, start time, end time and location are those that were originally set when the shift was added to the schedule. 
	The goal is that, if necessary, the corresponding shift in the timesheet can be immediately filled with the original values using this form. 
	However, edits can be made to any of the fields before marking the shift as worked.   
	
