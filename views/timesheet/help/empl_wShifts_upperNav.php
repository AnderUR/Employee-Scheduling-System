    <br><br><h3><b>Total Hours</b></h3>
    The total hours worked this period refers to the hours worked by the employee for one week. The given week is specified in the two date fields below. However, by default, they show the current week. The Worked Shift Table shows data for the given employee according to the week being viewed.
     
    <br><br><h3><b>View Period & Date Fields</b></h3>
    Clicking on the date or time fields opens a date picker. Once selections have been made as necessary, the user can click the button View Period to see data in the table from-to the selected dates. Note that the total hours will change accordingly as well.<br><br>
    <b>Important note:</b> Upon clicking the view period button, the start date will be used for all pages that filter with date. For example, if 2019/06/16 is selected, then the status table will show data for 2019/06/16.
	        
