    <br><br><h3><b>Select Semester</b></h3>
    The Schedule Shifts tab will always <b>default to the current semester</b>, which then controls the data shown in the <b>Employee Schedule table</b>. To change the semester, choose another from this dropdown.
    
    <br><br><h3><b>Calendar Icon</b></h3>
    The <b>calendar icon</b> can be clicked to view the calendar from CUNY for the semester being shown in Select Semester. <br>
    The link for this icon is entered through the <b>Add Semester form</b> in the Admin Panel > Semester Tab.
    
    <br><br><h3><b>Employee Schedule Table</b></h3>
    This table shows all the shifts for the given employee, according to the selected semester.
    
    <br><br><h3><b>Shifts Column</b></h3>
    The edit link in this column opens the <b>Edit Semester Schedule form</b>. 
    Changes in this form will be displayed in the <b>Employee Schedule Table</b>, in the <b>Schedule Table</b> and in the <b>Status Table</b>
    
    <br><br><h3><b>Day Column</b></h3>
    This column displays the day the shift is due. Note that if the shift repeats, this column will show "Every...", specifying the day and until when it will repeat. 
    Otherwise, it will display the one day the shift is due.
    
