    <br><br><h3><b>Show week of...</b></h3>
    When a date is selected from the date picker, the schedule table will show the week of the selected date. 
    Any date can be selected from the picker and if no data is available, the table will be empty.
    
    <br><br><h3><b>Sort by...</b></h3>
    There are currently two sorting methods, by Employee and by Area. <br>
    1. Sort by Area - By default, area is selected. The schedule table will show all employees for the given week grouped by the area where they work.<br>
    2. Sort by Employee - The schedule table will show employees alphabetically by last name. Each employee gets their own row of shifts and the Shifts Column is made available.
    
    <br><br><h3><b>This Week</b></h3>
    This button refreshes the schedule table to show shifts for the current day. 
    It is recommended to press this button to be sure that the data being viewed in the table is current.
    
    <br><br><br><img src="/LibServices/assets/ess_assets/img/helpIMG/tsheet_sch_upperNavArrows.png">
    
    <br><br><br><h3><b>Previous & Next Week Arrows</b></h3>
    These arrows change the schedule being shown to the previous or next week's schedule. The dates between the arrows show the range of the week being viewed, Sunday to Saturday.
    
