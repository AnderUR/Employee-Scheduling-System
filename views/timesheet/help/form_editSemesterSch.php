    <br><br><h3><b>Edit Semester Schedule Form</b></h3>
    <b>Opens by clicking the edit link in the Employee page > Semester Schedule Table </b><br><br>
    
    This form allows the user to <b>modify the schedule for the entire semester</b>. 
    Changes in this form will be visible in the Employee page > Semester Schedule Table, Schedule page > Schedule Table and Status page > Status Table for the shifts' date. 
    
    <br><br><h3><b>Substitute</b></h3> 
    Search for an employee using this input to substitute the current owner of the shift. <br><br>
    Use the input by typing the name of the employee you wish to use for the substitution. An autocomplete function will open with users. 
    Select the employee from the list and accept the prompt to make the substitution. Cancel if the selection was incorrect.<br><br>
    <b>When Repeat weekly is selected</b>, this shift will be substituted for <b>today</b> (assuming the shift is due today) <b>and all future shifts</b>.
    
    <br><br><h3><b>Repeat Weekly</b></h3>   
	<b>When the checkbox is unchecked</b> - The shift entered will be applied <b>once</b> for the selected date.<br>
	<b>When the checkbox is checked</b> - The shift will be repeated weekly until the date entered in the input below it. 
	It will be repeated by day of week, according to the due day.
	
    <br><br><h3><b>Save</b></h3>   
	Submits any edits made to the shift (note that substitute does not need to be saved, it automatically saves when an employee is selected). 
	This will update all schedule related tables.
	
    <br><br><h3><b>Remove</b></h3>   	
	<b>Removes all future occurrences of this shift</b> as a result of repeat weekly being used. 
	<b>To remove this shift only for this week, use the edit shift form from the status table in the status page.</b> 
	Note that shifts that have been worked cannot be removed. In order to remove the worked shifts, do so in the Edit Timesheet form first. 
	Then remove here as explained.
	
	
	
