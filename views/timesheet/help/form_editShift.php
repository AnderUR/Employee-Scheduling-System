    <br><br><h3><b>Edit Shift Form</b></h3>
	<b>Opens from the Status page > Status table edit link</b><br><br>
	
	This form opens when a shift has not been signed in. 
	Any changes to this form will only affect the shift being edited, not future shifts as a result of Repeat Weekly.
	For example, the <b>Remove button</b> will only remove the current shift.
	
    <br><br><h3><b>Set in Timesheet</b></h3>
	Opens the Set in Timesheet (Status page) form to mark the employee as signed in, or signed in and signed out.	
