    <br><br><h3><b>Login</b></h3>
    The ESS website cannot be accessed without credentials; users will always be asked to login, unless they choose to have the browser save their credentials. 
    
    <br><br><h3><b>Sign out</b></h3>
    When users sign out, they will be redirected to the LSS page. There they can click on <b>Resources</b> and click on <b>Employee Scheduling</b> to return to the ESS.
    
    <br><br><h3><b>Notifications</b></h3>
    In the notifications link, users will have access to submitted notifications by the admin from the <b>Admin Panel</b>, in the <b>Notifications Tab</b>.<br>
    Any future improvements or additions to notifications will be visible here. 
    
    <br><br><h3><b>Navigation Menu</b></h3>
    Users have two quick methods for navigating ESS:
    <ol>
        <li> The address bar of the browser can be used to access direct content, such as specific dates for the schedule table, sorted by labs: .../timesheet/scheduleLabIndex/2017-08-02.</li>
        <li> The navigation bar provides easy access for the main links of the ESS, such as the schedule page. The indicator arrow will tell the users what page they are currently viewing.</li>
    </ol>
