    <br><br><h3><b>Timesheet Table</b></h3>
    This table shows all the shifts the employees have worked for the current week.
    The data inside comes from three locations: <b>the signin/signout page, the edit timesheet form, or the set in timesheet form</b>.
    
    <br><br><h4><b>Shifts Column</b></h4>
    This column is only available if the sorting method is set to employee. Supervisors can click the view link to be <b>directed to the Employee page > Worked Shifts Tab for the given employee.</b>
