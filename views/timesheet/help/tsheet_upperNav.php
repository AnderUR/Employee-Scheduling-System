    <br><br><h3><b>Show week of...</b></h3>
    When a date is selected from the picker, the timesheet table will show the week of the selected date. 
    Any date can be selected from the picker and if no data is available, the table will be empty.
    
    <br><br><h3><b>Sort by...</b></h3>
    There are currently two sorting methods, by Employee and by Area. <br>
    1. Sort by Area - By default, area is selected. The timesheet table will show all employees for the given week grouped by the area where they have worked.<br>
    2. Sort by Employee - The timesheet table will show employees alphabetically by last name. Each employee get their own row of shifts and the Shifts Column is made available.
    
    <br><br><h3><b>This Week</b></h3>
    This button refreshes the timesheet table to show shifts for the current day.
    
    <br><br><br><img src="/LibServices/assets/ess_assets/img/helpIMG/tsheet_sch_upperNavArrows.png">
    
    <br><br><br><h3><b>Previous & Next Week Arrows</b></h3>
    These arrows change the timesheet being shown to the previous or next week's timesheet. The dates between the arrows show the range of the week being viewed, Sunday to Saturday.
    Note that the table will be empty if a week other than the current week is viewed or if no data is available. 
    
